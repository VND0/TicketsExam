        если есть две одинаковые рядом стоящие цифры, и слово 'NO', в противном случае.
        */
        Console.WriteLine("Эта программа определяет, верно ли, что введенное число содержит две одинаковые цифры.");
        Console.WriteLine("Обработка исключений проводилась только если этого просило задание или хотел автор кода. " +
