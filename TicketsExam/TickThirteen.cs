        Console.WriteLine();

        string num = AdditionalMethods.Input("Число: ");
        bool flag = false;
        for (int i = 1; i < num.Length; i++)
        {
            if (num[i] == num[i - 1])
            {
                flag = true;
                break;
            }
        }

        if (flag)
        {
            Console.WriteLine("YES");
        }
        else
        {
            Console.WriteLine("No");
        }
